        Instrument muzyczny to niemalże każde źródło dźwięku, które można wykorzystać do tworzenia muzyki.
        <br>Instrumenty muzyczne różnią się wyglądem, materiałem z jakiego są zbudowane, techniką grania, barwą czy skala dźwięków.
        <br>Instrumenty dzielą się na kolejno na:
        <ul>
            <li>grupy</li>
            <li>podgrupy</li>
            <li>rodzaje</li>
            <li>gatunki</li>
        </ul>
        <br>Jest to oficjalnie obowiązujący podział, zgodnie z którym mamy cztery grupy instrumentów:
        <ul>
            <li>chordofony</li>
            <li>aerofony</li>
            <li>membranofony</li>
            <li>idiofony</li>
        </ul>
        <br>Jednak tradycyjnie grupę membranofonów i idiofonów łączone są w jedna grupę instrumentów perkusyjnych, zatem najpowszechniejsza
        i najpopularniejsza klasyfikacja dzieli instrumenty muzyczne na:
        <ul>
            <li>Instrumenty strunowe</li>
            <li>Instrumenty dęte</li>
            <li>Instrumenty perkusyjne</li>
        </ul>
